#   (длинный метод, длинный список параметров)


class Unit:
    def __init__(self):
        pass

    def speeds(self):
        if self.state == 'fly':
            return 1.2
        return 0.5

    def direction(self, direction):
        speed = self.speeds()
        if direction == 'UP':
            self.field.set_unit(y=self.y + speed, x=self.x, unit=self)
        elif direction == 'DOWN':
            self.field.set_unit(y=self.y - speed, x=self.x, unit=self)
        elif direction == 'LEFT':
            self.field.set_unit(y=self.y, x=self.x - speed, unit=self)
        elif direction == 'RIGTH':
            self.field.set_unit(y=self.y, x=self.x + speed, unit=self)
